/*
 * ============================================================
 *  연습문제 02 - 동적 배열 (Dynamic Array)
 * ============================================================
 *
 * [핵심 개념]
 *
 * C의 일반 배열은 크기가 컴파일 타임에 고정됩니다.
 *   int arr[5];   ← 무조건 5개. 늘리거나 줄일 수 없음.
 *
 * Python은 리스트가 크기를 자동으로 조절합니다.
 *   arr = []
 *   arr.append(1)  # 알아서 공간 확보
 *
 * C에서 Python 리스트처럼 쓰려면 malloc으로 동적 배열을 만들어야 합니다.
 *
 * [배열 malloc 공식]
 *   타입 *ptr = (타입 *)malloc(sizeof(타입) * 개수);
 *
 * 예) 정수 5개짜리 동적 배열:
 *   int *arr = (int *)malloc(sizeof(int) * 5);
 *
 * [배열처럼 쓰는 방법]
 *   arr[0] = 10;   ← 가능! 포인터 + 인덱스 = 배열처럼 동작
 *   arr[1] = 20;
 *   *(arr + 2) = 30;  ← arr[2] = 30과 동일한 표현
 *
 * ============================================================
 */

#include <stdio.h>
#include <stdlib.h>

int main(void) {

    int n;
    printf("배열 크기를 입력하세요: ");
    scanf("%d", &n);

    /* -------------------------------------------------------
     * [문제 2-1] 정수 n개를 저장할 동적 배열 만들기
     *
     * 힌트: malloc(sizeof(int) * n)
     * 할당 실패 시 NULL을 반환하므로 NULL 체크 필수!
     * ------------------------------------------------------- */
    int *arr = NULL;

    /* TODO: arr에 int n개 크기의 메모리를 동적 할당하세요 */
    arr = malloc(sizeof(typeof(arr))*n);

    if (arr == NULL) {
        printf("메모리 할당 실패!\n");
        return 1;
    }

    /* -------------------------------------------------------
     * [문제 2-2] 배열에 값 채우기
     *
     * 동적으로 할당된 배열도 arr[i] 로 접근할 수 있습니다.
     * arr[i]  ←→  *(arr + i)   둘 다 같은 표현입니다.
     *
     * 아래 반복문을 완성하여 arr[i] = i * 10 으로 채우세요.
     * (arr[0]=0, arr[1]=10, arr[2]=20, ...)
     * ------------------------------------------------------- */
    for (int i = 0; i < n; i++) {
        /* TODO: arr[i]에 i * 10을 저장하세요 */
        arr[i]=i*10;
    }

    /* -------------------------------------------------------
     * [문제 2-3] 배열 출력하기
     * ------------------------------------------------------- */
    printf("동적 배열 내용: ");
    for (int i = 0; i < n; i++) {
        /* TODO: arr[i]를 출력하세요 */
        printf("%d ", arr[i]);
    }
    printf("\n");

    /* -------------------------------------------------------
     * [문제 2-4] 배열의 합 구하기
     *
     * 동적 배열도 일반 배열처럼 반복문으로 순회할 수 있습니다.
     * ------------------------------------------------------- */
    int sum = 0;
    for (int i = 0; i < n; i++) {
        /* TODO: sum에 arr[i]를 더하세요 */
        sum+=arr[i];
    }
    printf("합계: %d\n", sum);

    /* -------------------------------------------------------
     * [문제 2-5] 메모리 해제
     *
     * malloc으로 만든 배열은 반드시 free 해야 합니다.
     * 배열 전체를 free할 때는 arr[0]만 free해도 될까요?
     * → NO! 반드시 포인터(arr) 자체를 free해야 합니다.
     *   malloc이 배열 전체를 하나의 블록으로 할당했기 때문.
     * ------------------------------------------------------- */

    /* TODO: arr을 free하고 NULL로 초기화하세요 */
    free(arr);
    arr=NULL;

    printf("메모리 해제 완료\n");
    return 0;
}

