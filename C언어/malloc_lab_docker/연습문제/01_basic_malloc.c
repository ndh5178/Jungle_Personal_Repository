/*
 * ============================================================
 *  연습문제 01 - malloc/free 기초
 * ============================================================
 *
 * [핵심 개념]
 *
 * Python에서는 변수를 만들면 메모리가 자동으로 할당됩니다.
 *   x = 42   # 파이썬이 알아서 공간 만들고 42를 넣어줌
 *
 * C에서는 두 가지 방식이 있습니다.
 *   1) 스택(Stack): int x = 42;  → 함수가 끝나면 자동 소멸
 *   2) 힙(Heap):   malloc()      → 내가 직접 만들고 직접 없애야 함
 *
 * malloc(size)의 동작:
 *   - 힙에서 size 바이트만큼 빈 공간을 찾아서 예약함
 *   - 그 공간의 "시작 주소"를 포인터로 반환함
 *   - 실패하면 NULL을 반환함 → 반드시 NULL 체크 해야 함!
 *
 * free(ptr)의 동작:
 *   - malloc으로 예약한 공간을 운영체제에 돌려줌
 *   - free를 안 하면? → "메모리 누수(memory leak)" 발생!
 *
 * [주의]
 *   - free 후 ptr를 다시 쓰면 안 됨 (dangling pointer)
 *   - 같은 메모리를 두 번 free하면 안 됨 (double free)
 *   - free 후에는 ptr = NULL로 초기화하는 게 좋은 습관
 *
 * ============================================================
 */

