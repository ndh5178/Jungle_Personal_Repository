/*
 * ============================================================
 *  연습문제 04 - 2차원 배열 동적 할당
 * ============================================================
 *
 * [핵심 개념]
 *
 * Python: matrix = [[0]*cols for _ in range(rows)]  # 자동
 *
 * C에서 2차원 배열을 동적으로 만드는 방법:
 *
 *   방법 A - "포인터의 배열" (행 포인터 배열 방식)
 *
 *     int **matrix;
 *
 *     Step 1: 행(row) 포인터 배열 할당
 *       matrix = (int **)malloc(sizeof(int *) * rows);
 *                                        ↑ int* 하나의 크기
 *
 *     Step 2: 각 행에 열(col) 배열 할당
 *       for (i = 0; i < rows; i++)
 *           matrix[i] = (int *)malloc(sizeof(int) * cols);
 *
 *   메모리 구조:
 *     matrix → [ *row0 | *row1 | *row2 ]  ← 행 포인터 배열
 *                  ↓        ↓        ↓
 *               [0,1,2] [0,1,2] [0,1,2]  ← 실제 데이터
 *
 *   해제 순서 (중요!):
 *     1. 각 행(matrix[i]) 먼저 free
 *     2. 행 포인터 배열(matrix) free
 *     → 반대로 하면 각 행의 주소를 잃어버려 메모리 누수!
 *
 * ============================================================
 */

#include <stdio.h>
#include <stdlib.h>

int main(void) {

    int rows = 5, cols = 4;

    /* -------------------------------------------------------
     * [문제 4-1] 행 포인터 배열 할당
     *
     * matrix는 "int*를 담는 배열" 입니다.
     * 즉, int* 가 rows개 필요합니다.
     *
     * 크기: sizeof(int *) * rows
     * 타입: int **  (int*의 포인터)
     * ------------------------------------------------------- */
    int **matrix = NULL;

    /* TODO: matrix에 행 포인터 배열을 할당하세요 */
    //잘못된 값을 할당하면 어떻게 되는지
    matrix = malloc(sizeof(int*) * rows);

    if (matrix == NULL) {
        printf("행 포인터 배열 할당 실패!\n");
        return 1;
    }

    /* -------------------------------------------------------
     * [문제 4-2] 각 행에 열 배열 할당
     *
     * matrix[i]는 i번째 행의 시작 주소입니다.
     * 각 행에 cols개의 int를 저장할 공간을 할당해야 합니다.
     *
     * 실패 시 이미 할당된 행들을 free해야 합니다! (생략 가능)
     * ------------------------------------------------------- */
    for (int i = 0; i < rows; i++) {
        /* TODO: matrix[i]에 int cols개 크기의 배열을 할당하세요 */
        matrix[i] = malloc(sizeof(int) * cols);

        if (matrix[i] == NULL) {
            printf("%d번째 행 할당 실패!\n", i);
            /* 이미 할당된 행들 free (고급) */
            for (int j = 0; j < i; j++) free(matrix[j]);
            free(matrix);
            return 1;
        }
    }

    /* -------------------------------------------------------
     * [문제 4-3] 값 채우기: matrix[i][j] = i * cols + j
     *
     * matrix[i][j]  ←→  *(*(matrix + i) + j)
     * 일반 2차원 배열과 동일한 문법으로 접근 가능합니다.
     *
     * 결과:
     *   matrix[0][0]=0  matrix[0][1]=1  matrix[0][2]=2  matrix[0][3]=3
     *   matrix[1][0]=4  matrix[1][1]=5  ...
     *   matrix[2][0]=8  matrix[2][1]=9  ...
     * ------------------------------------------------------- */
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            /* TODO: matrix[i][j]에 i * cols + j를 저장하세요 */
            matrix[i][j] = i * cols + j;
        }
    }

    /* -------------------------------------------------------
     * [문제 4-4] 행렬 출력하기
     * ------------------------------------------------------- */
    printf("=== 2차원 배열 출력 ===\n");
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            /* TODO: matrix[i][j]를 "%3d " 형식으로 출력하세요 */
            printf("%3d ", matrix[i][j]);
        }
        printf("\n");
    }

    /* -------------------------------------------------------
     * [문제 4-5] 각 행의 합 구하기
     * ------------------------------------------------------- */
    printf("=== 각 행의 합 ===\n");
    for (int i = 0; i < rows; i++) {
        int row_sum = 0;
        for (int j = 0; j < cols; j++) {
            /* TODO: row_sum에 matrix[i][j]를 더하세요 */
            row_sum+=matrix[i][j];
        }
        printf("행 %d의 합: %d\n", i, row_sum);
    }

    /* -------------------------------------------------------
     * [문제 4-6] 메모리 해제 (순서 중요!)
     *
     * 반드시 이 순서로:
     *   1. for 루프로 각 행(matrix[i]) free
     *   2. 행 포인터 배열(matrix) free
     *
     * 왜 이 순서인가?
     *   matrix를 먼저 free하면 matrix[i]의 주소를 잃어버림
     *   → 각 행을 free할 방법이 없어짐 → 메모리 누수!
     * ------------------------------------------------------- */

    /* TODO: 각 행을 먼저 free하는 반복문을 작성하세요 */
    for (int i = 0; i < rows; i++) {
        free(matrix[i]);
    }

    /* TODO: 행 포인터 배열(matrix)을 free하고 NULL로 초기화하세요 */
    free(matrix);
    matrix=NULL;

    printf("메모리 해제 완료\n");
    return 0;
}

