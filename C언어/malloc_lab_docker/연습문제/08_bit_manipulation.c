/*
 * ============================================================
 *  연습문제 08 - 비트 연산 (malloc-lab 핵심!)
 * ============================================================
 *
 * [왜 필요한가?]
 *
 * malloc-lab에서 각 블록의 헤더(Header)는 딱 4바이트입니다.
 * 이 4바이트에 두 가지 정보를 동시에 담아야 합니다:
 *
 *   1. 블록 크기  (예: 24바이트)
 *   2. 할당 여부 (0 = free, 1 = allocated)
 *
 * 어떻게? → 크기는 항상 8의 배수 → 하위 3비트가 항상 0
 *           → 하위 1비트를 할당 비트로 사용!
 *
 * 헤더 비트 구조:
 *   [31 ........... 3] [2] [1] [0]
 *    <-- 크기 (29bit) -->  X   X  <alloc bit>
 *
 * 예) 크기=24(0x18), 할당됨(1):
 *   24 = 0b...00011000
 *        0b...00000001  (OR)
 *        ──────────────
 *        0b...00011001  = 0x19  ← 헤더에 저장되는 값
 *
 * ============================================================
 *
 * [mm.c에서 실제로 쓰이는 매크로들]
 *
 * #define WSIZE     4          // 워드 크기 (bytes)
 * #define DSIZE     8          // 더블 워드 크기
 * #define ALIGNMENT 8          // 정렬 단위
 *
 * // 크기와 할당비트를 합쳐서 헤더 값 만들기
 * #define PACK(size, alloc)  ((size) | (alloc))
 *
 * // 헤더 값에서 크기만 추출 (하위 3비트 제거)
 * #define GET_SIZE(p)   (GET(p) & ~0x7)
 *
 * // 헤더 값에서 할당비트만 추출 (하위 1비트)
 * #define GET_ALLOC(p)  (GET(p) & 0x1)
 *
 * // 헤더에 4바이트 읽기/쓰기
 * #define GET(p)       (*(unsigned int *)(p))
 * #define PUT(p, val)  (*(unsigned int *)(p) = (val))
 *
 * ============================================================
 */

